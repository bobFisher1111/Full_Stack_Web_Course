/*
    - SQL vs NOSQL:
        - SQL(Structured Query Language):
            - Relational
                - Table Structure
                - Requires Schema
                - Scales Vertically, will slow down and not good for scaling
                - Split up repeating data into own tables and connect with a primary key:
                    - Example 1:
                        - Customer Table
                        - Product Table 
                        - Orders Table

        - NOSQL(Not only Structured Query Language):
            - Non-Relational
                - Document Structure
                - More Flexible to changes
                - Scales horizontally, greate for scalablility
                - Good for one to many data, but difficult and slow with relational data
                - Example:
                    - Like one user with many post
*/
